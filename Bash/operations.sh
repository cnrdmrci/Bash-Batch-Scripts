sum=`expr 2 + 2`
sub=`expr 2 - 2`
div=`expr 2 / 2`
mul=`expr 2 \* 2`
mod=`expr 2 % 2`

echo "Sum: $sum"
echo "Sub: $sub"
echo "Div: $div"
echo "Mul: $mul"
echo "Mod: $mod"

